public class Loops1 {
    public static void main(String[] args) {
        // Print number from 20 - 10
        // Repetition - Loop
        // 1. Initialization point
        int i = 20;
        // 2. Condition
        while (i >= 10) {
            // 3. Statement to be repeated
            System.out.println(i);
            // 4. Increment/ Decrement statement
            i -= 2;
        }
    }
}
