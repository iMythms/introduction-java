public class ArraysEx1 {
    public static void main(String[] args) {

        // "Mytham", "Taha", "Ali", "Jasim"
        String[] arr = { "Mytham", "Taha", "Ali", "Jasim" };

        // Printing the Array
        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i] + " ");
        }
    }
}